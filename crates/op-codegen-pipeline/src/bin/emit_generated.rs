//! `emit_generated` — WAVE 1 stub of the OP → op-rs V3 emission binary.
//!
//! Runs the real harvest (`ruff_ruby_spo::extract_app_with_schema` →
//! `filter_to_core` → `op_codegen_pipeline::ogar_consumer::compile_op`)
//! and writes a small, DETERMINISTIC placeholder into the checked-in
//! landing zone `crates/op-generated/src/generated/mod.rs` — proving the
//! write path is real and reproducible without emitting real structs yet.
//! Real per-class emission (via the `ogar-render-askama` render leg) is
//! W2's job; this binary only carries the pipe.
//!
//! ```sh
//! RAILS_CORPUS_SRC=/home/user/openproject \
//!     cargo run -p op-codegen-pipeline --features ogar-emit --bin emit_generated
//! ```
//!
//! Without `RAILS_CORPUS_SRC` this prints a skip note and exits 0, so CI
//! without a corpus checkout is unaffected — same convention as
//! `tests/harvest_denominator_probe.rs`.

use std::path::{Path, PathBuf};
use std::process;

use ogar_vocab::ports::OpenProjectPort;
use op_codegen_pipeline::ogar_consumer::compile_op;
use op_codegen_pipeline::{filter_to_core, NAMESPACE};

fn main() {
    let Some(src) = std::env::var_os("RAILS_CORPUS_SRC") else {
        eprintln!(
            "emit_generated: RAILS_CORPUS_SRC not set — skipping emission (set it to a \
             Rails app root, e.g. an OpenProject checkout). Exiting 0."
        );
        return;
    };
    let corpus = Path::new(&src);

    // extract_app_with_schema -> ModelGraph (pre-filter) -> filter_to_core
    // -> compile_op::<OpenProjectPort> -> Vec<CompiledClass>. Mirrors
    // tests/harvest_denominator_probe.rs's call sequence exactly.
    let (mut graph, _report) = ruff_ruby_spo::extract_app_with_schema(corpus, NAMESPACE);
    filter_to_core(&mut graph);
    let compiled = compile_op::<OpenProjectPort>(&graph);
    let class_count = compiled.len();

    let out_path = generated_mod_path();
    if let Err(e) = std::fs::write(&out_path, render_placeholder(class_count)) {
        eprintln!("emit_generated: failed to write {}: {e}", out_path.display());
        process::exit(1);
    }

    eprintln!("emit_generated: wrote {} (GENERATED_CLASS_COUNT = {class_count})", out_path.display());
}

/// `crates/op-generated/src/generated/mod.rs`, resolved relative to this
/// crate's own manifest dir (via `CARGO_MANIFEST_DIR`, baked in at compile
/// time) rather than the process's current working directory — so the
/// binary writes the right file no matter where `cargo run`/the resulting
/// executable is invoked from.
fn generated_mod_path() -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR")).join("../op-generated/src/generated/mod.rs")
}

/// The W1 STUB placeholder body: a `@generated` header plus one
/// deterministic const. W2 replaces this with real per-class structs
/// rendered through `ogar-render-askama`; for now this only proves the
/// harvest -> write path is real and reproducible (same `compiled.len()`
/// in, same bytes out).
fn render_placeholder(class_count: usize) -> String {
    format!(
        "// @generated by emit_generated — do not edit by hand.\n\
         //\n\
         // WAVE 1 stub: proves the harvest -> write path is real and\n\
         // reproducible. W2 replaces this with real compiled structs\n\
         // rendered through ogar-render-askama.\n\
         \n\
         /// Number of `CompiledClass`es the W1 harvest produced\n\
         /// (`extract_app_with_schema` -> `filter_to_core` ->\n\
         /// `compile_op::<OpenProjectPort>`). A stub count, not yet real\n\
         /// emitted structs — see the crate doc comment in `lib.rs`.\n\
         pub const GENERATED_CLASS_COUNT: usize = {class_count};\n"
    )
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn render_placeholder_is_deterministic() {
        assert_eq!(render_placeholder(16), render_placeholder(16));
    }

    #[test]
    fn render_placeholder_embeds_the_count() {
        assert!(render_placeholder(16).contains("GENERATED_CLASS_COUNT: usize = 16;"));
    }
}
