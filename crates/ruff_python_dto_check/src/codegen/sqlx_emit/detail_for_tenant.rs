//! sqlx (axum + raw `sqlx::query_as`) emitter for the
//! [`HandlerKind::DetailForTenant`] kind.
//!
//! The seaorm sibling at `crate::codegen::emit_detail_for_tenant`
//! (`codegen/mod.rs:617`) emits an askama-template handler against a sea-orm
//! `Entity::find_by_id` + `ensure_tenant` pair. This emitter is API-first
//! (no view template) and binds the primary-key + tenant-scope columns
//! directly into a raw SQL `query_as`. The shape is byte-faithful to the
//! openproject-axum-sqlx target golden so the integration test trims output
//! and compares it to the expected file under
//! `tests/golden/codegen/sqlx/expected/detail_for_tenant.rs`.
//!
//! Inert until the orchestrator wires `pub mod detail_for_tenant;` into the
//! parent `sqlx_emit` module — the file is self-contained and pulls no
//! private helpers from `codegen::mod`.
//!
//! ## Tenant scoping policy
//!
//! The SQL column used for the tenant-scope predicate is **NOT**
//! `spec.tenant_column`. That field is the seaorm `Column` *variant* name
//! (PascalCase, e.g. `"ProjectId"`); in raw SQL we use the actual database
//! column, which conventionally is the snake_case lower form of the *first*
//! path parameter (e.g. `project_id`). Falling back to `tenant_id` keeps the
//! emitter honest for the degenerate single-param case.
//!
//! ## Primary-key policy
//!
//! The primary key is conventionally the **last** path parameter on a
//! detail-for-tenant route (`/projects/<project_id>/work_packages/<id>`),
//! and gets bound to `$1`. The scope parameter(s) follow as `$2..`.

use std::fmt::Write as _;

use crate::codegen::Emitted;
use crate::codegen::target::TargetSpec;
use crate::contract::{PathParam, RouteContract};

/// Emit a sqlx (axum + `sqlx::query_as`) detail-for-tenant handler for one
/// route contract against `spec`. The output is a complete, compileable Rust
/// file under the caller's choice of `handler_file` name; when the primary
/// model cannot be resolved against the spec, the handler is replaced with a
/// documented `EXTRACTOR-GAP` comment (no code), matching the seaorm
/// emitter's `unresolved_block` policy and the sqlx `list_for_tenant`
/// sibling.
pub fn emit_detail_for_tenant_sqlx(contract: &RouteContract, spec: &TargetSpec) -> Emitted {
    let fn_name = &contract.function;
    let axum = axum_path(&contract.path);
    let scoping = if contract.guards.is_empty() {
        "none".to_string()
    } else {
        contract.guards.join(", ")
    };

    let header = format!(
        "//! endpoint:     {endpoint}\n\
         //! flask path:   {flask}\n\
         //! axum path:    {axum}\n\
         //! method:       {method}\n\
         //! source:       {file}:{line}\n\
         //! handler_kind: detail_for_tenant\n\
         //! scoping:      {scoping}\n\
         //\n\
         // GENERATED by ruff_python_codegen — DO NOT EDIT.\n\
         // Modify target-spec/openproject-axum-sqlx.toml instead.\n",
        endpoint = contract.id,
        flask = contract.path,
        method = contract.methods.join(","),
        file = contract.provenance.file,
        line = contract.provenance.line_start,
    );

    let (referenced_models, body) = match primary_model(contract, spec) {
        Some((py_class, module_path)) => {
            // Build the model struct path inline. The sqlx target does NOT
            // append `::Model` (that suffix is seaorm-specific); the struct
            // *is* the row type implementing `FromRow`.
            let model_struct = format!("{}::{}::{py_class}", spec.models_root, module_path);
            let table = pluralize_snake(py_class);

            let params: &[PathParam] = &contract.inputs.path_params;
            let (primary_name, scope_params) = split_primary_and_scope(params);
            let path_extractor = path_extractor(params);
            let where_clause = where_clause(primary_name, scope_params);
            let bindings = bind_lines(primary_name, scope_params);

            let body = format!(
                "\n\
                 use axum::extract::{{Path, State}};\n\
                 use axum::response::IntoResponse;\n\
                 \n\
                 use crate::error::{{ApiError, ApiResult}};\n\
                 use crate::extractors::{{AppState, AuthenticatedUser, HalResponse}};\n\
                 use {model_struct};\n\
                 \n\
                 pub async fn {fn_name}(\n\
                 \x20   State(state): State<AppState>,\n\
                 \x20   _user: AuthenticatedUser,\n\
                 \x20   {path_extractor},\n\
                 ) -> ApiResult<impl IntoResponse> {{\n\
                 \x20   let pool = state.pool()?;\n\
                 \x20   let item: {py_class} = sqlx::query_as::<_, {py_class}>(\n\
                 \x20       r#\"SELECT * FROM {table} WHERE {where_clause}\"#,\n\
                 \x20   )\n\
                 {bindings}\
                 \x20   .fetch_optional(&pool)\n\
                 \x20   .await\n\
                 \x20   .map_err(|e| ApiError::internal(format!(\"Database error: {{}}\", e)))?\n\
                 \x20   .ok_or_else(|| ApiError::not_found(\"{py_class}\", {primary_name}))?;\n\
                 \n\
                 \x20   Ok(HalResponse(item))\n\
                 }}\n",
            );
            (vec![py_class.to_string()], body)
        }
        None => (
            contract.data.models.clone(),
            unresolved_comment(contract, spec),
        ),
    };

    Emitted {
        handler_rs: format!("{header}{body}"),
        view_html: None,
        handler_file: format!("{}__{}.rs", contract.family, fn_name),
        view_file: None,
        referenced_models,
        provided_context_keys: Vec::new(),
    }
}

/// Flask `<int:foo>` / `<foo>` → axum `:foo`. Mirrors the helper in
/// `codegen::mod` (private there); inlined to keep this module
/// self-contained — see the file-level doc-comment.
fn axum_path(flask_path: &str) -> String {
    let mut out = String::with_capacity(flask_path.len());
    let mut rest = flask_path;
    while let Some(open) = rest.find('<') {
        out.push_str(&rest[..open]);
        let after = &rest[open + 1..];
        let Some(close) = after.find('>') else {
            out.push_str(&rest[open..]);
            return out;
        };
        let inner = &after[..close];
        let name = inner.split_once(':').map_or(inner, |(_, n)| n);
        out.push(':');
        out.push_str(name);
        rest = &after[close + 1..];
    }
    out.push_str(rest);
    out
}

/// Resolve the first mapped model in `contract.data.models`. Returns
/// `(python_class, module_path)` — the python class is kept verbatim so the
/// generated sqlx struct reference is `crate::models::<module>::<Class>`
/// (the sqlx target does NOT append `::Model`; that suffix is seaorm-only).
fn primary_model<'a>(
    contract: &'a RouteContract,
    spec: &'a TargetSpec,
) -> Option<(&'a str, &'a str)> {
    for m in &contract.data.models {
        if let Some((k, v)) = spec.models.get_key_value(m) {
            return Some((k.as_str(), v.module_path.as_str()));
        }
    }
    None
}

/// Map a Flask path converter to the sqlx-bind Rust type. The sqlx target
/// uses `i64` (Postgres `BIGINT`) for integers — distinct from the seaorm
/// emitter which uses `i32`. Mirrors the soft-delete sibling's choice so
/// emitted handlers compose across kinds.
fn flask_conv_to_sqlx_rust(converter: Option<&str>) -> &'static str {
    match converter {
        Some("int") => "i64",
        Some("float") => "f64",
        _ => "String",
    }
}

/// Build the `Path(...)` extractor line, handling 0 / 1 / N params. With one
/// param we emit `Path(x): Path<i64>`; with several we emit the tuple form
/// `Path((a, b)): Path<(i64, i64)>` — matching axum's tuple-destructure
/// convention.
fn path_extractor(params: &[PathParam]) -> String {
    match params {
        [] => "Path(_): Path<()>".to_string(),
        [p] => format!(
            "Path({}): Path<{}>",
            p.name,
            flask_conv_to_sqlx_rust(p.converter.as_deref())
        ),
        many => {
            let names = many
                .iter()
                .map(|p| p.name.as_str())
                .collect::<Vec<_>>()
                .join(", ");
            let tys = many
                .iter()
                .map(|p| flask_conv_to_sqlx_rust(p.converter.as_deref()))
                .collect::<Vec<_>>()
                .join(", ");
            format!("Path(({names})): Path<({tys})>")
        }
    }
}

/// The primary key is conventionally the *last* path parameter on a
/// detail-for-tenant route (`/<scope>/.../<id>`). When no path params are
/// present, fall back to `id` so the emitted code still compiles.
fn split_primary_and_scope(params: &[PathParam]) -> (&str, &[PathParam]) {
    match params.split_last() {
        Some((last, rest)) => (last.name.as_str(), rest),
        None => ("id", &[]),
    }
}

/// Build the SQL `WHERE` clause: `id = $1 [AND <scope> = $N]*`. Scope params
/// are the path params *preceding* the primary key, in URL order, bound to
/// `$2, $3, …`. The column names in SQL are the path-param names verbatim
/// (`project_id`, `account_id`, …) — already in snake_case lower because
/// Flask path params follow that convention.
fn where_clause(primary_name: &str, scope_params: &[PathParam]) -> String {
    let mut out = format!("{primary_name} = $1");
    for (idx, p) in scope_params.iter().enumerate() {
        let _ = write!(out, " AND {} = ${}", p.name, idx + 2);
    }
    out
}

/// Build the `.bind(...)` lines in `$1, $2, …` order (primary key first,
/// then scope params in URL order). Each line includes its own leading
/// indent and trailing newline so the call site can splice without
/// additional formatting.
fn bind_lines(primary_name: &str, scope_params: &[PathParam]) -> String {
    let mut out = String::new();
    let _ = writeln!(out, "    .bind({primary_name})");
    for p in scope_params {
        let _ = writeln!(out, "    .bind({})", p.name);
    }
    out
}

/// `WorkPackage` → `work_packages`, `Customer` → `customers`. Naïve
/// snake_case + `s` suffix, sufficient for the contract surface this
/// emitter targets (the spec deliberately keeps the mapping inline; a full
/// inflector lives in the orchestrator). Already-plural roots ending in
/// `s` survive untouched.
fn pluralize_snake(class: &str) -> String {
    let snake = to_snake_case(class);
    if snake.ends_with('s') {
        snake
    } else {
        format!("{snake}s")
    }
}

fn to_snake_case(class: &str) -> String {
    let mut out = String::with_capacity(class.len() + 4);
    for (i, ch) in class.chars().enumerate() {
        if ch.is_ascii_uppercase() {
            if i > 0 {
                out.push('_');
            }
            out.push(ch.to_ascii_lowercase());
        } else {
            out.push(ch);
        }
    }
    out
}

/// EXTRACTOR-GAP comment: emitted in place of code when no mapped model is
/// found, matching the seaorm `unresolved_block` policy (Iron Rule 1: never
/// emit `todo!()`/`unimplemented!()` into a compilable production path).
fn unresolved_comment(contract: &RouteContract, spec: &TargetSpec) -> String {
    format!(
        "// EXTRACTOR-GAP: no mapping in target {target} for models {models:?}\n\
         //\n\
         // The contract referenced these models but the target spec has no\n\
         // [models.<Class>] entry. Add a mapping in target-spec/{target}.toml\n\
         // (or extend the extractor profile if the reference was missed in\n\
         // the AST pass). Calibration lint: unmapped-model.\n",
        target = spec.id,
        models = contract.data.models,
    )
}
