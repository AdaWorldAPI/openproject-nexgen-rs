//! sqlx (axum + raw `sqlx::query_as`) emitter for the
//! [`HandlerKind::ListForTenant`] kind.
//!
//! The seaorm emitter in `crate::codegen` produces an askama-template handler
//! against a sea-orm `Entity`; this emitter is API-first (no view) and binds
//! the tenant column + pagination directly into a raw SQL `query_as`. The
//! shape is byte-faithful to the openproject-axum-sqlx target golden so the
//! integration test trims output and the spec compares it to the expected
//! file under `tests/golden/codegen/sqlx/expected/`.
//!
//! Inert until the orchestrator wires `pub mod list_for_tenant;` into the
//! parent module — the file is self-contained and pulls no private helpers
//! from `codegen::mod`.

use crate::codegen::Emitted;
use crate::codegen::target::TargetSpec;
use crate::contract::RouteContract;

/// Emit a sqlx (axum + `sqlx::query_as`) list-for-tenant handler for one
/// route contract against `spec`. The output is a complete, compileable Rust
/// file under the caller's choice of `handler_file` name; when the primary
/// model cannot be resolved against the spec, the handler is replaced with a
/// documented `EXTRACTOR-GAP` comment (no code), matching the seaorm
/// emitter's `unresolved_block` policy.
pub fn emit_list_for_tenant_sqlx(contract: &RouteContract, spec: &TargetSpec) -> Emitted {
    let fn_name = &contract.function;
    let axum = axum_path(&contract.path);
    let method = contract.methods.join(",");
    let scoping = if contract.guards.is_empty() {
        "none".to_string()
    } else {
        contract.guards.join(", ")
    };

    let header = format!(
        "//! endpoint:     {endpoint}\n\
         //! flask path:   {flask}\n\
         //! axum path:    {axum}\n\
         //! method:       {method}\n\
         //! source:       {file}:{line}\n\
         //! handler_kind: list_for_tenant\n\
         //! scoping:      {scoping}\n\
         //\n\
         // GENERATED by ruff_python_codegen — DO NOT EDIT.\n\
         // Modify target-spec/{target_id}.toml instead.\n\
         \n",
        endpoint = contract.id,
        flask = contract.path,
        file = contract.provenance.file,
        line = contract.provenance.line_start,
        target_id = spec.id,
    );

    let (referenced_models, body) = match primary_model(contract, spec) {
        Some((py_class, module_path)) => {
            let model_struct = format!("{}::{}::{py_class}", spec.models_root, module_path);
            let model_use = model_struct.clone();
            let table = pluralize(&pascal_to_snake(py_class));
            let tenant_col = pascal_to_snake(&spec.tenant_column);
            let path_param = path_param_for_binding(contract, &tenant_col);
            let order_col = contract.data.order_by.as_deref().unwrap_or("id");
            let order_dir = contract
                .data
                .order_dir
                .as_deref()
                .unwrap_or("asc")
                .to_uppercase();

            let body = format!(
                "use axum::extract::{{Path, State}};\n\
                 use axum::response::IntoResponse;\n\
                 \n\
                 use crate::error::{{ApiError, ApiResult}};\n\
                 use crate::extractors::{{AppState, AuthenticatedUser, HalResponse, Pagination}};\n\
                 use {model_use};\n\
                 \n\
                 pub async fn {fn_name}(\n\
                 \x20   State(state): State<AppState>,\n\
                 \x20   _user: AuthenticatedUser,\n\
                 \x20   Path({path_param}): Path<i64>,\n\
                 \x20   pagination: Pagination,\n\
                 ) -> ApiResult<impl IntoResponse> {{\n\
                 \x20   let pool = state.pool()?;\n\
                 \x20   let items: Vec<{py_class}> = sqlx::query_as::<_, {py_class}>(\n\
                 \x20       r#\"SELECT * FROM {table} WHERE {tenant_col} = $1 ORDER BY {order_col} {order_dir} LIMIT $2 OFFSET $3\"#,\n\
                 \x20   )\n\
                 \x20   .bind({path_param})\n\
                 \x20   .bind(pagination.page_size as i64)\n\
                 \x20   .bind(pagination.offset as i64)\n\
                 \x20   .fetch_all(&pool)\n\
                 \x20   .await\n\
                 \x20   .map_err(|e| ApiError::internal(format!(\"Database error: {{}}\", e)))?;\n\
                 \n\
                 \x20   let total: i64 = sqlx::query_scalar::<_, i64>(\n\
                 \x20       r#\"SELECT COUNT(*) FROM {table} WHERE {tenant_col} = $1\"#,\n\
                 \x20   )\n\
                 \x20   .bind({path_param})\n\
                 \x20   .fetch_one(&pool)\n\
                 \x20   .await\n\
                 \x20   .map_err(|e| ApiError::internal(format!(\"Database error: {{}}\", e)))?;\n\
                 \n\
                 \x20   Ok(HalResponse(serde_json::json!({{\n\
                 \x20       \"_type\": \"Collection\",\n\
                 \x20       \"total\": total,\n\
                 \x20       \"count\": items.len(),\n\
                 \x20       \"pageSize\": pagination.page_size,\n\
                 \x20       \"offset\": pagination.offset,\n\
                 \x20       \"_embedded\": {{ \"elements\": items }},\n\
                 \x20   }})))\n\
                 }}\n",
            );
            (vec![py_class.to_string()], body)
        }
        None => (contract.data.models.clone(), unresolved_comment(contract, spec)),
    };

    Emitted {
        handler_rs: format!("{header}{body}"),
        view_html: None,
        handler_file: format!("{}__{}.rs", contract.family, fn_name),
        view_file: None,
        referenced_models,
        provided_context_keys: Vec::new(),
    }
}

/// Flask `<int:foo>` / `<foo>` → axum `:foo`. Mirrors the helper in
/// `codegen::mod` (private there); inlined here to keep this module
/// self-contained — see the file-level doc-comment.
fn axum_path(flask_path: &str) -> String {
    let mut out = String::with_capacity(flask_path.len());
    let mut rest = flask_path;
    while let Some(open) = rest.find('<') {
        out.push_str(&rest[..open]);
        let after = &rest[open + 1..];
        let Some(close) = after.find('>') else {
            out.push_str(&rest[open..]);
            return out;
        };
        let inner = &after[..close];
        let name = inner.split_once(':').map_or(inner, |(_, n)| n);
        out.push(':');
        out.push_str(name);
        rest = &after[close + 1..];
    }
    out.push_str(rest);
    out
}

/// Resolve the first mapped model in `contract.data.models`. Returns
/// `(python_class, module_path)` — the python class is kept verbatim so the
/// generated sqlx struct reference is `crate::models::<module>::<Class>`
/// (the sqlx target does NOT append `::Model`; that suffix is seaorm-only).
fn primary_model<'a>(
    contract: &'a RouteContract,
    spec: &'a TargetSpec,
) -> Option<(&'a str, &'a str)> {
    for m in &contract.data.models {
        if let Some((k, v)) = spec.models.get_key_value(m) {
            return Some((k.as_str(), v.module_path.as_str()));
        }
    }
    None
}

/// Pick the path-param name to bind into the tenant filter. The first path
/// param is the natural choice; if the contract has no path params, fall
/// back to the tenant-column name itself (a degenerate case the
/// `list_for_tenant` classifier excludes, but kept honest here).
fn path_param_for_binding(contract: &RouteContract, fallback: &str) -> String {
    contract
        .inputs
        .path_params
        .first()
        .map(|p| p.name.clone())
        .unwrap_or_else(|| fallback.to_string())
}

/// `PascalCase` → `snake_case`. Inlined (the existing
/// `codegen::snake_to_pascal` is the inverse direction).
fn pascal_to_snake(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 2);
    for (i, c) in s.chars().enumerate() {
        if c.is_ascii_uppercase() {
            if i > 0 {
                out.push('_');
            }
            out.push(c.to_ascii_lowercase());
        } else {
            out.push(c);
        }
    }
    out
}

/// Naive English pluralizer for table names. Sufficient for the WoA-rs /
/// openproject model surface, where the historical seaorm port also assumes
/// trivial `+s` and already-plural roots like `news` survive untouched.
fn pluralize(snake: &str) -> String {
    if snake.ends_with('s') {
        snake.to_string()
    } else {
        format!("{snake}s")
    }
}

/// EXTRACTOR-GAP comment: emitted in place of code when no mapped model is
/// found, matching the seaorm `unresolved_block` policy (Iron Rule 1: never
/// emit `todo!()`/`unimplemented!()` into a compilable production path).
fn unresolved_comment(contract: &RouteContract, spec: &TargetSpec) -> String {
    format!(
        "// EXTRACTOR-GAP: no mapping in target {target} for models {models:?}\n\
         //\n\
         // The contract referenced these models but the target spec has no\n\
         // [models.<Class>] entry. Add a mapping in target-spec/{target}.toml\n\
         // (or extend the extractor profile if the reference was missed in\n\
         // the AST pass). Calibration lint: unmapped-model.\n",
        target = spec.id,
        models = contract.data.models,
    )
}
